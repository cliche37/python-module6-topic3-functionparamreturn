def multiply_string(message, n):
    """Returns a string 'n' amount of times

    Simply multiplies a string n amount of times and returns it.

    :param string message: Message/String to multiply
    :param int n: Number of times to multiply message/string
    :return: If message is 'message' and n is '3', return will be messagemessagemessage
    :rtype: stringe

    """

    return str(message) * n
